CREATE TABLE LM_REPLEAT_RESULT
(
  QUERY_ID     VARCHAR2(100 BYTE),
  USER_ID      NUMBER(10),
  REAL_NAME    VARCHAR2(100 BYTE),
  ORGAN_ID     VARCHAR2(100 BYTE),
  ORGAN_NAME   VARCHAR2(100 BYTE),
  TOTAL        NUMBER(10),
  RE_TOTAL     NUMBER(10),
  UN_RE_TOTAL  NUMBER(10),
  CREATE_DATE  DATE                             DEFAULT sysdate,
  START_DATE   DATE,
  END_DATE     DATE
