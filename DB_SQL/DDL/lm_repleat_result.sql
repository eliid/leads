CREATE INDEX IDX_ANYRE_ORGAN_ID ON LM_REPLEAT_RESULT
(ORGAN_ID);


CREATE INDEX IDX_ANYRE_QUERY_ID ON LM_REPLEAT_RESULT
(QUERY_ID);
