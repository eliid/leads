CREATE TABLE LINK_ERP_REPORT
(
  ID             NUMBER(10)                     NOT NULL,
  CLIENT_ID      NUMBER(10),
  L_NAME         VARCHAR2(250 BYTE),
  L_TEL_PHONE    VARCHAR2(20 BYTE),
  L_CREATE_TIME  DATE,
  L_PRO_CODE     VARCHAR2(60 BYTE),
  L_CATENA_NAME  VARCHAR2(250 BYTE),
  L_SPEC_NAME    VARCHAR2(250 BYTE),
  L_SCHEME_NAME  VARCHAR2(550 BYTE),
  CREATE_TIME    DATE                           DEFAULT sysdate,
  DB_SOURCE      VARCHAR2(2 BYTE)
