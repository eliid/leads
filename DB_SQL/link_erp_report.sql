COMMENT ON COLUMN LINK_ERP_REPORT.L_CATENA_NAME IS '系别名称';

COMMENT ON COLUMN LINK_ERP_REPORT.L_SPEC_NAME IS '型号名称';

COMMENT ON COLUMN LINK_ERP_REPORT.L_SCHEME_NAME IS '商务政策别名';

COMMENT ON COLUMN LINK_ERP_REPORT.CREATE_TIME IS '数据创建时间';

COMMENT ON COLUMN LINK_ERP_REPORT.DB_SOURCE IS '数据来源 22:捷众 24:结义';



